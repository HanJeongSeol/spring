package polymorphism1;

public class LgTV {
	public void on() {
		System.out.println("LgTV - 전원ON");
	}
	
	public void off() {
		System.out.println("LgTV - 전원OFF");
	}
	
	public void soundUp() {
		System.out.println("LgTV - 소리UP");
	}
	
	public void soundDown() {
		System.out.println("LgTV - 소리DOWN");
	}
}
